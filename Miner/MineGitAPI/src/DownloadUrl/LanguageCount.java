package DownloadUrl;

public class LanguageCount {
String name;
Double count;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public Double getCount() {
	return count;
}
public void setCount(Double count) {
	this.count = count;
}
public LanguageCount(String name, Double count) {
	super();
	this.name = name;
	this.count = count;
}

}
